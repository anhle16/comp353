    <h1> Public Health Information and Recommendations</h1>
    <p dir="ltr"><span>Sick person under home isolation is asked to follow the instructions below to prevent the spread of COVID-19 to other people around him/her:</span></p>
<ul>
<li dir="ltr">
<p dir="ltr"><span>Do not go to school, work, a childcare center or any other public space.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Do not use public transport.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>If the sick person lives alone and has no help to get essentials such as food or</span><span><br /></span><span>medication, use phone/online grocery and pharmacy home delivery services.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Do not receive visitors at home.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>If the sick person lives with other people who are not infected by COVID-19:</span></p>
</li>
<ul>
<li dir="ltr">
<p dir="ltr"><span>Remain alone in one room of the house as often as possible and close to the door.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Eat and sleep alone in one room of the house.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>If possible, use a bathroom reserved for the sick person's sole use. Otherwise, disinfect the bathroom after every use.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Avoid as much as possible contact with other people in the home. If this is impossible, wear a mask. If a mask is not available, stay at least two meters away from other people.</span></p>
</li>
</ul>
<li dir="ltr">
<p dir="ltr"><span>Open a window to air out the sick person's room and home often (weather permitting).</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Do not go to a medical clinic unless you have first obtained an appointment and have notified the clinic that you have COVID-19. If you need to go to the emergency room (eg, if you have difficulty breathing), contact 911 and tell the person that you are sick with COVID-19.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Wear a mask when someone is in the same room as you.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Wear a mask if you must leave your home to seek medical care, you must first</span><span><br /></span><span>notify the medical clinic (or 911, if it is an emergency) that you have COVID-19.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>If you need to cough, sneeze, or blow your nose:</span></p>
</li>
<ul>
<li dir="ltr">
<p dir="ltr"><span>If you have a disposable tissue use it to cough, sneeze or blow your nose then discard the tissue in the garbage, and then wash your hands with soap and water.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>If you do not have disposable tissues, cough, or sneeze in the crook of your arm.</span></p>
</li>
</ul>
<li dir="ltr">
<p dir="ltr"><span>Wash your hands:</span></p>
</li>
<ul>
<li dir="ltr">
<p dir="ltr"><span>Wash your hands often with soap under warm running water for at least 20</span><span><br /></span><span>seconds.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Use an alcohol- based hand rub if soap and water are not available.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Wash your hands before eating and whenever your hands look dirty.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>After using the toilet, put the lid down before flushing and then wash your</span><span><br /></span><span>hands.</span></p>
</li>
</ul>
<li dir="ltr">
<p dir="ltr"><span>Do not share plates, utensils, glasses, towels, bed sheets or clothes with others.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>Watch your symptoms and take your temperature every day:</span></p>
</li>
<ul>
<li dir="ltr">
<p dir="ltr"><span>Take your temperature every day at the same hour.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>If you are taking medication for fever wait at least four hours before taking your temperature.</span></p>
</li>
</ul>
<li dir="ltr">
<p dir="ltr"><span>Directives in case of severe symptoms:</span></p>
</li>
<ul>
<li dir="ltr">
<p dir="ltr"><span>If your symptoms worsen call 514-644-4545 or call your doctor.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>If you have difficulty breathing, or shortness of breath or chest pain call 911 and inform them you may be infected by COVID-19.</span></p>
</li>
</ul>
<li dir="ltr">
<p dir="ltr"><span>If someone close or caregiver helps you with your daily activities, then before</span><span><br /></span><span>helping you, the person must wash his/her hand, wear a mask and put-on disposable gloves. After helping you, the person must take off the gloves and put them in a garbage bin with a lid, wash his/her hands, take off the mask and put it in a garbage bin with a lid, and wash his/her hands again.</span></p>
</li>
<li dir="ltr">
<p dir="ltr"><span>For help with psychosocial matters, contact 811.</span></p>
</li>
</ul>
